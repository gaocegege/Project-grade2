package domain;

public class Importance {
	//key word class
	private String word;
	private int importance;
	public void setWord(String word) {
		this.word = word;
	}
	public String getWord() {
		return word;
	}
	public void setImportance(int importance) {
		this.importance = importance;
	}
	public int getImportance() {
		return importance;
	}
}
